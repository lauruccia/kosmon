<h1>Politica AML/KYC</h1>
<p class="subtitle">Anti-Money Laundering e Know Your Customer — Versione 1.0</p>

<h2>1. Scopo</h2>
<p>KMoney adotta misure di verifica dell'identità dei propri aderenti conformemente ai principi generali di prevenzione del riciclaggio di denaro e del finanziamento del terrorismo, pur operando come circuito privato di compensazione e non come istituto finanziario vigilato.</p>

<h2>2. Documenti richiesti per KYC</h2>
<ul>
    <li>Visura camerale aggiornata (non oltre 6 mesi)</li>
    <li>Documento d'identità valido del titolare/legale rappresentante</li>
    <li>Codice fiscale dell'azienda</li>
    <li>Eventuale documentazione aggiuntiva su richiesta del Gestore</li>
</ul>

<h2>3. Processo di verifica</h2>
<p>Dopo l'invio dei documenti, il team KMoney completa la verifica entro 1-3 giorni lavorativi. In caso di documenti incompleti o non leggibili, verrà richiesta un'integrazione. Il conto rimane in stato "in attesa" fino all'approvazione.</p>

<h2>4. Monitoraggio delle transazioni</h2>
<p>Il Gestore monitora le transazioni alla ricerca di pattern anomali quali: importi sproporzionati rispetto all'attività dichiarata, frazionamento artificioso di operazioni, frequenza inusuale. In caso di segnalazioni sospette, il Gestore si riserva il diritto di sospendere il conto e richiedere documentazione integrativa.</p>

<h2>5. Obblighi dell'aderente</h2>
<p>L'aderente si impegna a: fornire informazioni veritiere, aggiornare i documenti in caso di variazioni societarie rilevanti, non cedere l'accesso al conto a terzi non autorizzati.</p>

<h2>6. Aggiornamento della politica</h2>
<p>Questa politica è soggetta a revisione periodica. Gli aderenti verranno notificati di modifiche significative con almeno 15 giorni di preavviso.</p>
